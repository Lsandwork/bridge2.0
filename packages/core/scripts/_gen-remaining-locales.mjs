#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const EN_FILE = path.resolve(__dirname, "../src/locales/en.ts");

function parsePortalKeys(content) {
  const keys = [];
  const re = /"((?:therapist|parent)\.[^"]+)":\s*"((?:[^"\\]|\\.)*)"/g;
  for (const m of content.matchAll(re)) keys.push(m[1]);
  return keys;
}

const KEY_ORDER = parsePortalKeys(fs.readFileSync(EN_FILE, "utf8"));

function writeLocaleFile(code, data) {
  const lines = [`export const ${code} = {`];
  for (const key of KEY_ORDER) {
    const val = data[key];
    if (val == null) throw new Error(`[${code}] missing ${key}`);
    lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(val)},`);
  }
  lines.push("};");
  lines.push("");
  fs.writeFileSync(path.join(__dirname, `_portal-locale-${code}.mjs`), lines.join("\n"));
  console.log(`${code}: ${Object.keys(data).length} keys`);
}

